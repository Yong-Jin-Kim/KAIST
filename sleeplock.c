void
initsleeplock(struct sleeplock *lk, char *name)
{
  initlock(&lk->lk, "sleep lock");
  lk->name = name;
  lk->locked = 0;
  lk->pid = 0;
}

void
acquiresleep(struct sleeplock *lk)
{
  struct proc *cur;
  acquire(&lk->lk);
  while (lk->locked) {
    if(lk->head == 0)
      lk->head = myproc();
    else {
      cur = lk->head;
      while(cur->next != 0) cur = cur->next;
      cur->next = myproc();
    }
    myproc()->next = 0;
    sleep(myproc(), &lk->lk);
    //sleep(lk, &lk->lk);
  }
  lk->locked = 1;
  lk->pid = myproc()->pid;
  release(&lk->lk);
}

void
releasesleep(struct sleeplock *lk)
{
  acquire(&lk->lk);
  lk->locked = 0;
  lk->pid = 0;
  wakeup(lk->head);
  if(lk->head != 0) lk->head = lk->head->next;
  //wakeup(lk);
  release(&lk->lk);
}

int
holdingsleep(struct sleeplock *lk)
{
  int r;
  
  acquire(&lk->lk);
  r = lk->locked && (lk->pid == myproc()->pid);
  release(&lk->lk);
  return r;
}
